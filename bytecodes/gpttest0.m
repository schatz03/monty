# Push examples
push 5          # Pushes the value 5 onto the stack
push "Hello"    # Pushes the string "Hello" onto the stack
push True       # Pushes the boolean value True onto the stack
push [1, 2, 3]  # Pushes the list [1, 2, 3] onto the stack

# Pull examples
pull x          # Pulls the top value from the stack and stores it in the variable x
pull y          # Pulls the top value from the stack and stores it in the variable y
pull result     # Pulls the top value from the stack and stores it in the variable result
pull [a, b]     # Pulls the top list from the stack and stores its elements in variables a and b
